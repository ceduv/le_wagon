#================ HTTP REQUEST =============

# An HTTP request is plain text sent by the client 
# to request something from the server.

# It contains three to four parts:

a verb or method (GET, POST, PATCH or DELETE)
a url (e.g: https://www.google.com/search?q=lewagon)
headers (metadata built by the client)
a body (data coming from a form) for POST, PATCH and PUT requests